// Integers in Swift
var num = 10
print(num)

// Strings in Swift
var name:String // Type declaration
name = "Bhavik Jadav" // Value Assigning
print(name)

var name2 = "Mehul" // Direct Assigning value without declaring the type of the variable
print(name2)

name = "B C Jadav" // Changing the value of the variable (var is Mutable)
print(name)

// Swift Constants
let final_name = "C Jadav"
// final_name = "BCJ"
print(final_name)

// Floating point numbers
let marks = 98.02
print(marks)

// Booleans
let levelPassed = false
print(levelPassed)



